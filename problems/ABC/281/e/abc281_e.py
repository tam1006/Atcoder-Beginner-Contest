def solve(N, M, K, A):
    tmp = A[:M]
    tmp.sort()
    L = SortedMultiset(tmp[:K])
    if M == K:
        R = SortedMultiset([float('inf')])
    else:
        R = SortedMultiset(tmp[K:])
    ans = [sum(tmp[:K])]

    for i in range(N-M):
        if A[i] in L:
            L.discard(A[i])
            if A[i+M] < R[0]:
                L.add(A[i+M])
                ans.append(ans[-1] - A[i] + A[i+M])
            else:
                r = R[0]
                L.add(r)
                R.discard(r)
                R.add(A[i+M])
                ans.append(ans[-1] - A[i] + r)
        else:
            R.discard(A[i])
            if A[i+M] > L[K-1]:
                R.add(A[i+M])
                ans.append(ans[-1])
            else:
                l = L[K-1]
                R.add(l)
                L.discard(l)
                L.add(A[i+M])
                ans.append(ans[-1] - l + A[i+M])

    return ans



